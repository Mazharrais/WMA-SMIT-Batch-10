var question = document.querySelector('.question');
var option1 = document.querySelector('#option1');
var option2 = document.querySelector('#option2');
var option3 = document.querySelector('#option3');
var option4 = document.querySelector('#option4');
var submit = document.querySelector('#submit');
var answers = document.querySelectorAll('.answer');
var showScore = document.querySelector('#showScore');


var index = 0;
var score = 0;
function loadQuestion(){
    var allQuestion = questionsList[index];
    question.innerText = allQuestion.question;
    option1.innerText = allQuestion.option1;
    option2.innerText = allQuestion.option2;
    option3.innerText = allQuestion.option3;
    option4.innerText = allQuestion.option4;


    



}
loadQuestion()


function getCheckAnswer(){
    var answer;
    answers.forEach((curAnsElem)=>{
      if(curAnsElem.checked){
        answer = curAnsElem.id
      }
     
    })  
    return answer;
};

function deselectAll(){
   answers.forEach((curAnsElem) => curAnsElem.checked = false);
}


submit.addEventListener('click', () =>{
    var checkedAnswer = getCheckAnswer();
   console.log(checkedAnswer);
   if(checkedAnswer === questionsList[index].answer){
    score++;
    console.log("mazhar",score);
   };
   
    index++;

    deselectAll();


    if(index < questionsList.length){
        loadQuestion();
    }else{
        showScore.innerHTML = `
      <h3> You Scored ${score}/${questionsList.length}  </h3>
      <button class="btn" onclick="location.reload()">Try Again </button>
      
      `;
      showScore.classList.remove('scoreArea');
    }

});